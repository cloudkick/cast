var httpUtil = require('util/http');
var httpConstants = require('http/constants');

function endpoints(req, res) {
  httpUtil.returnJson(res, 200, { 'services': httpConstants.APPS });
}

function register(app, apiVersion) {
  app.get('/', endpoints);
}

exports.register = register;
